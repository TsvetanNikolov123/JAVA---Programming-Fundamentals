package p02_PracticeFloatingPoints;

import java.math.BigDecimal;

public class PracticeFloatingPoints {
    public static void main(String[] args) {
        BigDecimal firstNum = new BigDecimal("3.141592653589793238");
        double secondNum = 1.60217657d;
        BigDecimal thirdNum = new BigDecimal("7.8184261974584555216535342341");

        System.out.println(firstNum);
        System.out.println(secondNum);
        System.out.println(thirdNum);
    }
}
